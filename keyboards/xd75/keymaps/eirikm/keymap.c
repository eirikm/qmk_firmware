const macro_t *action_get_macro(keyrecord_t *record, uint8_t id, uint8_t opt)
{
  // MACRODOWN only works in this function
      switch(id) {
        case 0:
          if (record->event.pressed) {
            register_code(KC_RSFT);
            #ifdef BACKLIGHT_ENABLE
              backlight_step();
            #endif
          } else {
            unregister_code(KC_RSFT);
          }
        break;
      }
    return MACRO_NONE;
};

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) {
        switch(keycode) {
            case MLARROW:
                SEND_STRING("<-");
                return false;
            case MRARROW:
                SEND_STRING("->");
                return false;
            case MRFARRO:
                SEND_STRING("=>");
                return false;
            case MLPIPE:
                SEND_STRING("<|");
                return false;
            case MRPIPE:
                SEND_STRING("|>");
                return false;
            case MLCOMP:
                SEND_STRING("<<");
                return false;
            case MRCOMP:
                SEND_STRING(">>");
                return false;
        }
    }
    return true;
};

